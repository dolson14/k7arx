def RCPair(f,target,Rerr=1,Cerr=1):
    values = np.zeros((len(standardR[Rerr]),len(standardC[Cerr])))
    for i,r in enumerate(standardR[Rerr]):
        for j,c in enumerate(standardC[Cerr]):
            values[i,j] = f(r,c)
    diff = abs(values-target)
    best = np.where(diff==diff.min())[0]
    return standardR[Rerr][best[0]],standardC[Cerr][best[1]],diff[best]/target*100

